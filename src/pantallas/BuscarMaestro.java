    void buscar(String valor){
        String [] titulos={"#","Nombre","Apellido Paterno","Apellido Materno","Sexo","Cargo sindical","Nivel","Escuela de adscripcion","Region","Delegacion","Celular","Correo"};
        String [] registros = new String [12];
        model = new DefaultTableModel(null,titulos);
        conectar cc = new conectar();
        Connection cn = cc.conexion();
        String sql="";
        if(valor.equals("")){
            sql="SELECT * FROM maestro";
        }else{
            sql = "SELECT * FROM maestro WHERE nombre LIKE '%" + valor + "%' OR apellido_paterno LIKE '%" + valor + "%' OR apellido_materno LIKE '%" + valor + "%' OR delegacion LIKE '%" + valor + "%' OR sexo LIKE '%" + valor + "%'";
        }
    try {
        Statement st = cn.createStatement();
        ResultSet rs = st.executeQuery(sql);
        
        while(rs.next()){
            registros[0]=rs.getString("id");
            registros[1]=rs.getString("nombre");
            registros[2]=rs.getString("apellido_paterno");
            registros[3]=rs.getString("apellido_materno");
            registros[4]=rs.getString("sexo");
            registros[5]=rs.getString("cargo_sindical");
            registros[6]=rs.getString("nivel");
            registros[7]=rs.getString("escuela_adscripcion");
            registros[8]=rs.getString("region");
            registros[9]=rs.getString("delegacion");
            registros[10]=rs.getString("celular");
            registros[11]=rs.getString("correo");
            model.addRow(registros);
            
        }
        tmaestros.setModel(model);
        tmaestros.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_OFF);
        tmaestros.getColumnModel().getColumn(0).setPreferredWidth(100); 
        tmaestros.getColumnModel().getColumn(1).setPreferredWidth(200);
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null,ex);
    }
        
    }
