    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed
    conectar cc = new conectar();
    Connection cn = cc.conexion();
    try {
        PreparedStatement pst = cn.prepareStatement("Update maestro SET nombre='"+txtnombre.getText()+"',apellido_paterno='"+txtapp.getText()+"',apellido_materno='"+txtapm.getText()+"' WHERE id='"+txtbuscarnombre.getText()+"'");
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null,"Datos Actualizados");
        buscar("");
    } catch (SQLException ex) {
        Logger.getLogger(BuscarMaestro.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null,"Error al actualizar");
    }
    }//GEN-LAST:event_btnactualizarActionPerformed

